// Ejercicio 2: Cálculo del área de un triángulo utilizando function expression (función anónima)

// En este ejercicio, se utiliza una función expresiva o función anónima para calcular el área de un 
// triángulo. La función recibe la base y la altura como parámetros, realiza el cálculo del área y devuelve 
// el resultado. Se realiza la llamada a la función y se muestra el resultado en la consola.

function calcularAreaT(base,altura){
    return (base*altura)/2;
}